#include "../../includes/libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	len;

	len = ft_strlen(src);
	if (!(0 < dstsize))
		return (len);
	while (--dstsize && *src)
		*dst++ = *src++;
	*dst = 0;
	return (len);
}
