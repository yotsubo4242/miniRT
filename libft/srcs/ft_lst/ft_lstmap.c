#include "../../includes/libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_lst;
	t_list	*node;
	t_list	*fed_content;

	new_lst = NULL;
	while (lst)
	{
		fed_content = f(lst->content);
		node = ft_lstnew(fed_content);
		if (!node)
		{
			del(fed_content);
			ft_lstclear(&new_lst, del);
			return (NULL);
		}
		ft_lstadd_back(&new_lst, node);
		lst = lst->next;
	}
	return (new_lst);
}
